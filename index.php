  <!-- JavaScript for Read More / Read Less -->
  <script>
    document.addEventListener("DOMContentLoaded", function () {
      const maxLength = 200;

      document.querySelectorAll(".read-more-text").forEach(function (para) {
        const fullText = para.innerHTML.trim();

        if (fullText.length > maxLength) {
          const shortText = fullText.slice(0, maxLength) + "... ";
          const moreLink = document.createElement("a");
          moreLink.href = "#";
          moreLink.textContent = "Read More";
          moreLink.className = "read-more-toggle";
          para.innerHTML = shortText;
          para.appendChild(moreLink);

          moreLink.addEventListener("click", function (e) {
            e.preventDefault();
            if (moreLink.textContent === "Read More") {
              para.innerHTML = fullText + ' ';
              const lessLink = document.createElement("a");
              lessLink.href = "#";
              lessLink.textContent = "Read Less";
              lessLink.className = "read-more-toggle";
              para.appendChild(lessLink);

              lessLink.addEventListener("click", function (e) {
                e.preventDefault();
                para.innerHTML = shortText;
                para.appendChild(moreLink);
              });
            }
          });
        }
      });
    });
  </script>



